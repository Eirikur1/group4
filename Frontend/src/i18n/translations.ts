const translations = { en, es };

export function getTranslation(lang: "en" | "es", key: TranslationKey, params?: { count?: number }): string {
  const s = translations[lang][key] ?? en[key];
  if (params?.count != null && typeof s === "string" && s.includes("{{count}}")) {
    return s.replace("{{count}}", String(params.count));
  }
  return s;
}
